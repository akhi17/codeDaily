package akhi.io.misc;

public class Parent {
    public void eat() {
        System.out.println("Human is eating");
    }

    private void walk() {
        System.out.println("Human is walking");
    }

    protected void sleep() {
        System.out.println("Human is sleeping");
    }

    void breathe() {
        System.out.println("Human is breathing");
    }
}
